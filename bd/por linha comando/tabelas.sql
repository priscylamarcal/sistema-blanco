CREATE TABLE ESTADOS (
    CODESTADO  INTEGER NOT NULL PRIMARY KEY,
    ESTADO     VARCHAR(80) NOT NULL,
    UF         VARCHAR(3),
    DATACAD    TIMESTAMP,
    ULTALT     TIMESTAMP,
    CODUSU     INTEGER,
    CODPAIS    INTEGER NOT NULL,
    FOREIGN KEY (CODPAIS) REFERENCES PAISES(CODPAIS)
);

